<h1>Extraction</h1>

<h2>Situation</h2>
<blockquote>A key member of the resistance is hiding close to enemy lines. Aid in his rescue before the vital information he has falls into enemy hands!</blockquote>

<h2>Objective</h2>
<p>
  A key member of the resistance is hiding under objective #2, you must move to within 6” of this objective with at least two infantry units including an officer at which point the resistance operative will reveal themselves (place another model on the objective, at which point they join the officers unit) if there is no enemy unit within 6”.
  Your primary objective is to move this operative off your board edge.
  Your secondary objective is for your officer to survive after having revealed the operative.
</p>