<h1>The Mole</h1>

<h2>Intel</h2>
<blockquote>HQ has made contact with a friendly local close to enemy lines. The local has important information on the enemy positions. Aid in his rescue before the vital information he has falls into enemy hands!</blockquote>

<h2>Setup</h2>
<p>
  Just before beginning the first turn of the game, after all deployment is finished (including snipers, spotters and observers), you place a model representing the lone local on the table, following the same set-up instructions for the objective in the Hold Until Relieved scenario on page 114 of the Bolt Action rulebook.
  Special rules: The local has no weapons and cannot Run in the first turn of the game.
</p>

<h2>Objective</h2>
<p>
  The local needs to leave the table by moving into contact with your own table edge.
</p>
