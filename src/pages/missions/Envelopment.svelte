<h1>Envelopment</h1>

<h2>Situation</h2>
<blockquote>We need to threaten the enemy's communications and line of retreat. Flank and make a push to the enemy's rear.</blockquote>

<h2>Objective</h2>
<p>
  During the game have a unit move onto the table from outflank and end its turn in the enemy's table half.
</p>