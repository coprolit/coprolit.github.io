Mission:
<h1>Take Prisoners</h1>

<h2>INTEL</h2>
<blockquote>You are tasked with taking a prisoner for intel</blockquote>

<h2>OBJECTIVE</h2>
<p>Defeat any enemy infantry or artilery unit in close combat and you will have a prisoner. Move this prisoner behind the frontline before the end of the game.</p>

<h2>VICTORY</h2>
<p>Score one victory point if you keep the prisoner alive and under control.</p>
