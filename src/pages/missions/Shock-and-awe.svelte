<h1>Shock and awe</h1>

<h2>Situation</h2>
<blockquote>We must use overwhelming power and spectacular display of force to try and achieve rapid dominance over the enemy and destroy their will to fight.</blockquote>

<h2>Objective</h2>
<p>At the <strong>end of turn 3</strong> you must have <strong>3 or more units</strong> fully or partially within the opponent's table half.</p>

