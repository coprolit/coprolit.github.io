<h1>Shock and awe</h1>

<h2>Intel</h2>
<blockquote>We must use overwhelming power and spectacular display of force to try and achieve rapid dominance over the enemy and destroy their will to fight.</blockquote>

<h2>Objective</h2>
<p>At the end of turn 3 you must have 3 or more units fully or partially within the opponent's table half.</p>
