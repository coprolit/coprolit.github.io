<h1>Take Prisoners</h1>

<h2>Intel</h2>
<blockquote>You are tasked with taking a prisoner for intel.</blockquote>

<h2>Objective</h2>
<p>Defeat any enemy infantry or artillery unit in close combat to capture a prisoner. Add the enemy model to your assaulting unit. The prisoner is unarmed. Keep the prisoner alive and under control - at least one model of your unit and the prisoner must survive - to the end of the game.</p>
