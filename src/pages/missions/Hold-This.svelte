<h1>Hold This!</h1>

<h2>Situation</h2>
<blockquote>We have seized a key strategic position. Hold it until relieved.</blockquote>

<h2>Setup</h2>
<p>
  During setup, before deployment, you place an objective marker (roughly 2” in diameter) within 12” of your table edge.
</p>

