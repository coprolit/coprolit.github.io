<h1>Hold This!</h1>

<h2>Intel</h2>
<blockquote>We have seized a key strategic position. Hold it until relieved.</blockquote>

<h2>Setup</h2>
<p>
  During setup, before deployment, you place an objective marker (roughly 2” in diameter) in your deployment zone (or within 6” of your table edge).
</p>

<h2>Objective</h2>
<p>
  At the end of every turn of the game a model of a friendly unit must be within 3” of the objective marker.
</p>
